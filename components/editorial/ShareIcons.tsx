// Icon for a share channel key from lib/share.ts; email is a generic stroke
// icon, the rest are brand glyphs. Returns null for unknown keys so a future
// channel without an icon degrades gracefully.
export function ChannelIcon({ channel }: { channel: string }) {
  if (channel === "email") return <EmailIcon />;
  const brand = BRAND_PATHS[channel];
  return brand ? <Brand d={brand.d} color={brand.color} /> : null;
}
